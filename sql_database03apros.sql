WITH CostCalculation AS (
    SELECT 
        з.id AS заказ_id,
        з.номер_заказа,
        зс.продукция_id,
        зс.количество AS кол_во_продукции,
        зс.цена AS цена_продукции,
        зс.количество * зс.цена AS сумма_строки,
        (
            SELECT SUM(сп.количество * м.цена)
            FROM Спецификация сп
            INNER JOIN Материалы м ON сп.материал_id = м.id
            WHERE сп.продукция_id = зс.продукция_id
        ) AS себестоимость_единицы
    FROM Заказы з
    INNER JOIN Заказы_Строки зс ON з.id = зс.заказ_id
)
SELECT 
    cc.номер_заказа,
    п.наименование AS Продукция,
    cc.кол_во_продукции AS Количество,
    cc.цена_продукции AS Цена_за_единицу,
    cc.сумма_строки AS Стоимость_продукции,
    cc.кол_во_продукции * cc.себестоимость_единицы AS Себестоимость_материалов,
    cc.сумма_строки - (cc.кол_во_продукции * cc.себестоимость_единицы) AS Прибыль
FROM CostCalculation cc
INNER JOIN Продукция п ON cc.продукция_id = п.id
ORDER BY cc.номер_заказа;
GO